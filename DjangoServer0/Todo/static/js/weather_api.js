const getTempFromProxy = async() => {
  const pro = await fetch('http://127.0.0.1:6999/gettemp/');
  const res = await pro.json();
  const temp = res.temp
  return temp
}

const loadTempHTML = (temp) => {
  const jbt = document.querySelector('#jbt')
  jbt.innerHTML = getFullHTML(temp)
}

